package laba2var26;
/** Human class contains parametrs of orderer oad tailor */
public class Human {
	private String name;
	private double height;
	private double waist;
	private double chest;
	
	public void setName(String _name) {
		this.name = _name;
	}
	
	public void setHeight(double _height) {
		this.height = _height;
	}
	
	public void setWaist(double _waist) {
		this.waist = _waist;
	}
	
	public void setChest(double _chest) {
		this.chest = _chest;
	}
	
	public double getHeight() {
		return height;
	}
	
	public double getWaist() {
		return waist;
	}
	
	public double getChest() {
		return chest;
	}
	
	public String getName() {
		return name;
	}
}
